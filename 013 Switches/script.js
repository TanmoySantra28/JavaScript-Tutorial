//Switch

let day = 8;

switch(day){
    case 1:
        console.log("Sunday");
        break;
    case 2:
        console.log("Monday");
        break;   
    case 3:
        console.log("Tuesday");
        break;
    case 4:
        console.log("Wedensday");
        break;
    case 5:
        console.log("Thursday");
        break;
    case 6:
        console.log("Friday"); 
        break;
    case 7:
        console.log("Saturday");
        break;
    default:
        console.log(`${day} is not a day`);
}