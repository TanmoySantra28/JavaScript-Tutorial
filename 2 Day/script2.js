let y = 32;
y = y - 5; 
console.log(`Subtraction = ${y}`);
y -= 5;
console.log(`Subtraction = ${y}\n`);

let m = 32;
m = m * 2;
console.log(`Multiply = ${m}`);
m *= 2;
